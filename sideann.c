int sideann(int digit, unsigned char cbuf[], int cbuf_inc, float nibs, int start, int complement, int idir) {
    int i, j, k, n, nl, index;

	nl = 0;
	index = 0;
/*	printf("nibs= %f, start= %d, digit= %d, complement= %d\n",
			nibs,start,digit,complement); */
	if( nibs < 150 ) {
		if( start == 1 )
			n = 6;
		else
			n = 3;
		for( i=0; i<n; i++ ) {
			if( complement == 0 )
				for( j=0; j<2; j++ ) cbuf[index+j] = 0x0;
			else
				for( j=0; j<2; j++ ) cbuf[index+j] = 0xff;
			index = index + cbuf_inc;
			nl++;
		}
		i = digit;
		for( j=0; j<8; j++ ) {
			if( complement == 0 )
				for( k=0; k<2; k++ ) cbuf[index+k] = bits100[i][j][k];
			else
				for( k=0; k<2; k++ ) cbuf[index+k] = ~bits100[i][j][k];
			index = index + cbuf_inc;
			nl++;
		}
		return(nl);
	}
	if( nibs < 200 ) {
		if( start == 1 )
			n = 8;
		else
			n = 4;
		for( i=0; i<n; i++ ) {
			if( complement == 0 )
				for( j=0; j<3; j++ ) cbuf[index+j] = 0x0;
			else
				for( j=0; j<3; j++ ) cbuf[index+j] = 0xff;
			index = index + cbuf_inc;
			nl++;
		}
		i = digit;
		for( j=0; j<12; j++ ) {
			if( complement == 0 )
				for( k=0; k<3; k++ ) cbuf[index+k] = bits200[i][j][k];
			else
				for( k=0; k<3; k++ ) cbuf[index+k] = ~bits200[i][j][k];
			index = index + cbuf_inc;
			nl++;
		}
		return(nl);
	}
	if( nibs < 650 ) {
		if( start == 1 )
			n = 16;
		else
			n = 9;
		for( i=0; i<n; i++ ) {
			if( complement == 0 )
				for( j=0; j<5; j++ ) cbuf[index+j] = 0x0;
			else
				for( j=0; j<5; j++ ) cbuf[index+j] = 0xff;
			index = index + cbuf_inc;
			nl++;
		}
		i = digit;
		for( j=0; j<26; j++ ) {
			if( complement == 0 )
				if( idir > 0 ) for( k=0; k<5; k++ ) cbuf[index+k] = bits300[i][j][k];
				else for( k=0; k<5; k++ ) cbuf[index+k] = rbits300[i][j][k];
			else
				if( idir > 0 ) for( k=0; k<5; k++ ) cbuf[index+k] = ~bits300[i][j][k];
				else for( k=0; k<5; k++ ) cbuf[index+k] = ~rbits300[i][j][k];
			index = index + cbuf_inc;
			nl++;
		}
		return(nl);
	}
    return nl;
}
